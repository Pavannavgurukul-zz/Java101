package oprators;

public class ComparisonOperators {
    public static void main(String[] args) {
        //Here are some examples of Comparison Operators
        int a = 6;
        int b = 4;

        System.out.println(a==b); // It should print ==> false
        System.out.println(a!=b); // It should print ==> true ,because a and b is not equle to each other.
        System.out.println(a < b); // It should print ==> false
        System.out.println(a > b); // It should print ==> true
        System.out.println(a<=b); // It should print ==> false
        System.out.println(a>=b); // It should print ==> true
    }
}
